#Implementing membership operators.
import os
os.system('clear')

x = 'Hello World'
y = {'key1':'a','key2':'b'}

print('***Membership Operators***')
print(f'X = {x}\nY = {y}\n')
print(f'H in x: ',"H" in x)
print(f'world in x: ',"world" in x)
print(f'key1 in y: ',"key1" in y)
print(f'a in y: ' ,"a" in y)
print(f'3 not in y: ',3 not in y)
print(f'Python not in x: ' ,"Python" not in x)

